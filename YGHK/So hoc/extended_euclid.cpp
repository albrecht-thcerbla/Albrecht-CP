/// gcd(a, b) = g -> luon tim duoc (x, y) thoa man ax + by = g.

int x, y, cnt, d;

void euclid(int a, int b) {
	if (!b) {
		d = a;
		x[0] = 1;
		y[0] = 0;
		return;
	}
	euclid(b, a % b);
	++cnt;
	int tmp = x;
	x = y;
	y = tmp - (a / b) * y;
}
int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int a, b; cin >> a >> b;
  euclid(a, b);
  cout << d << '\n';
  cout << "x, y: " << x << ", " << y << '\n';	
  return 0;
}

