/**
 * 26 ky tu
 * cnt[c][i]: den i co bao nhieu c?
 * cnt[c][i] = cnt[c][i - 1] + 1 neu s[i] == 'c';
 * cnt[c][i] = cnt[c][i - 1];
 * => cnt[c][r] - cnt[c][l - 1];
 **/

int dp[30][1000005];
int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int n, q; cin >> n >> q;
  string s; cin >> s;
  s = ' ' + s;
  for (int i = 1; i <= n; i++) {
  	s[i] = s[i] - 'a' + 1;
  	for (int j = 1; j <= 26; j++) {
  		if (j == s[i]) dp[j][i] = dp[j][i - 1] + 1;
  		else dp[j][i] = dp[j][i - 1];
  	}
  }
  while (q--) {
  	char c; cin >> c;
  	int l, r; cin >> l >> r;
  	c = c - 'a' + 1;
  	cout << dp[c][r] - dp[c][l - 1] << '\n';
  }
  return 0;
}

