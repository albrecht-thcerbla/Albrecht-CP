#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int32_t main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  int tt; cin >> tt;
  while (tt--) {
  	int n; cin >> n;
  	int64_t sum = 0, ans[2] = {}, mi[2] = {}, sum_even = 0;
  	vector<int> a(n);
  	for (int i = 0; i < n; i++) {
  		cin >> a[i];
  		if (i & 1) {
  			sum += a[i];
  			ans[1] = max(ans[1], sum - mi[1]);
  			mi[1] = min(mi[1], sum);
  		} else {
  			sum -= a[i];
  			ans[0] = max(ans[0], sum - mi[0]);
  			mi[0] = min(mi[0], sum);
  			sum_even += a[i];
  		}
  	}
  	cout << sum_even + max(ans[0], ans[1]) << '\n';
  }
  return 0;
}

