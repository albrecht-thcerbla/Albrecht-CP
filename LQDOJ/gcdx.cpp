const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int gcd(int a, int b) {
	if (b == 0) return a;
	return gcd(b, a % b);
}
int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int n, p; cin >> n >> p;
  int cnt = 0;
  for (int i = 1; i <= n; i++) {
  	if (gcd(i, n) == p) ++cnt;
  }
  cout << cnt;
  return 0;
}

