#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int n; cin >> n;
  vector<int> v;
  v.emplace_back(n);
  for (int i = 2; i <= sqrt(n); i++) {
  	if (n % i == 0) {
  		if (n / i != i) {
  			v.emplace_back(n / i);
  			v.emplace_back(i);
  		} else v.emplace_back(i);
  	}
  }
  int ans = 0;
  for (int i = 0; i < sz(v); i++) {
  	int cnt = 0;
  	if (v[i] > 4) {
  		for (int j = 2; j <= sqrt(v[i]); j++) {
  			if (v[i] % j == 0) {
  				if (v[i] / j != j) cnt += 2;
  				else cnt++;
  			}
  		}
  		if (cnt == 2) ++ans;
  	}
  }
  cout << ans;
  return 0;
}

