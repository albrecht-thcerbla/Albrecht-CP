#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  vector<int> v;
  int cnt = 0;
  for (int n; cin >> n; ) {
  	v.emplace_back(n);
  }
  for (int i = 1; i < sz(v) - 1; i++) {
  	if (v[i] > v[i - 1] && v[i] > v[i + 1]) ++cnt;
  }
  cout << cnt;
  return 0;
}

