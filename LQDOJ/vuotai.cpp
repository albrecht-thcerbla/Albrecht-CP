#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int n, k; cin >> n >> k;
  vector<int> a(n);
  int mx = -1e9;
  long long sum = 0;
  for (int i = 0; i < n; i++) {
  	cin >> a[i];
  	sum += a[i];
  	mx = max(mx, a[i]);
  }
  int tmp = (mx < k) ? 0 : mx - k;
  sum = sum - mx + tmp;
  cout << sum + 1;
  return 0;
}

