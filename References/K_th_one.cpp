#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int n, m, sz = 1;
vector<int> a, t;

void build() {
	for (int i = 0; i < n; i++) t[i + sz] = a[i];
	for (int i = sz - 1; i > 0; i--) t[i] = t[i << 1] + t[i << 1 | 1];
}
void update(int p) {
	for (t[p += sz] ^= 1; p > 1; p >>= 1) t[p >> 1] = t[p] + t[p ^ 1];
}
int query(int k) {
	int p = 1;
	while (p < sz) {
		// left
		if (t[p << 1] > k) p = p << 1;
		else { // right
			k -= t[p << 1]; // da bo qua tong ben trai roi (vi ko co kha nang chua pt 1 thu k) nen phai tru di
			p = p << 1 | 1;
		}
	}
  // node la co chi so > size
	return (p - sz);
}
int32_t main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
	cin >> n >> m;
	while (sz < n) sz <<= 1;
	a.resize(sz); t.assign(sz << 1, 0);
	for (int i = 0; i < n; i++) cin >> a[i];
	build();
	while (m--) {
		int ty; cin >> ty;
		if (ty == 1) {
			int x; cin >> x;
			update(x);
		} else {
			int k; cin >> k;
			cout << query(k) << '\n'; 
		}
	}  
  return 0;
}

