#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int main() {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  // bai 17
  string s; cin >> s;
  int odd = 0, even = 0;
  for (int i = 0; i < sz(s); i++) {
  	if ((s[i] ^ '0') % 2 == 0) ++even;
  	else ++odd;
  }
  if (even == odd) cout << "YES" << '\n';
  else cout << "NO";
  return 0;
}

