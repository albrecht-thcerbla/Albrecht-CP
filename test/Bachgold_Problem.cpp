#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

vector<int> lp(100000), pr;
void sieve() {
  for (int i = 2; i <= 100000; i++) {
    if (!lp[i]) {
      lp[i] = i;
      pr.emplace_back(i);
    }
    for (int j = 0; i * pr[j] <= 100000; j++) {
      lp[i * pr[j]] = pr[j];
      if (lp[i] == pr[j]) break;
    }
  }
}
int main() {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("D:\\Albrecht-CP 2025\\input.inp", "r", stdin);
  freopen("D:\\Albrecht-CP 2025\\output.out", "w", stdout);
  sieve();
  int n; cin >> n;
  int cnt = 0, tmp = n;
  for (int i = 0; i < sz(pr) && pr[i] < n; i++) {
    while (tmp - pr[i] > 1) {
      tmp -= pr[i];
      ++cnt;
    }
  }
  cout << cnt + 1 << '\n';
  for (int i = 0; i < cnt; i++) cout << 2 << " ";
  cout << tmp;
  return 0;
}

