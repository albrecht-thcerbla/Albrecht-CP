#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int32_t main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  int tt; cin >> tt;
  while (tt--) {
    int n, m; cin >> n >> m;
    vector<int> a(n), v(n, 0);
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < m; i++) {
      int p; cin >> p;
      v[p - 1] = 1;
    }
    bool ok = true;
    while (ok) {
      ok = false;
      for (int i = 0; i < n - 1; i++) {
        if (v[i] && a[i] > a[i + 1]) {
          swap(a[i], a[i + 1]);
          ok = true;
        }
      }
    }
    ok = true;
    for (int i = 0; i < n - 1; i++) {
      if (a[i] > a[i + 1]) {
        ok = false;
        break;
      }
    }
    if (ok) cout << "YES" << '\n';
    else cout << "NO" << '\n';
  }
  return 0;
}

