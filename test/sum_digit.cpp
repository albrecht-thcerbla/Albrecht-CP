#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

int main () {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("input.inp", "r", stdin);
  freopen("output.out", "w", stdout);
  long long n; cin >> n;
  int even = 0, odd = 0;
  while (n) {
  	int tmp = n % 10;
  	if (tmp % 2 == 1) ++odd;
  	else ++even;
  	n /= 10;
  }
  if (even == odd) cout << "YES";
  else cout << "NO";
  return 0;
}

