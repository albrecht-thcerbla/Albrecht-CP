#include <bits/stdc++.h>
using namespace std;

#define sz(x) (int)x.size()
#define all(x) x.begin(), x.end()

const int mod = 1e9 + 7;
const int maxn = 1e5 + 7;

// 1 -> 1
// 2 -> 20
// 3 -> 210
// 4 -> 310

int main() {
  ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
  freopen("D:\\Albrecht-CP 2025\\input.inp", "r", stdin);
  freopen("D:\\Albrecht-CP 2025\\output.out", "w", stdout);
  int n; cin >> n;
  string ans;
  for (int mask = 1; mask < (1 << 10); mask++) {
    int sum = 0;
    for (int d = 0; d <= 9; d++) if (mask & (1 << d)) sum += d;
    if (sum != n) continue;
    string cur;
    for (int d = 9; ~d; --d) if (mask & (1 << d)) cur.push_back(char(d + '0'));
    if (ans.empty() || sz(cur) > sz(ans) || sz(cur) == sz(ans) && cur > ans) ans = cur;
  }
  cout << ans;
  return 0;
}

